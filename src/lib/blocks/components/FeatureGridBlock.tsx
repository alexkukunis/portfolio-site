export function RenderFeatureGrid({ content }: RenderProps<'feature-grid'>) {
  const v = content as FeatureGridContent;

  return (
    <div className="my-16">
      {v.title && (
        <div className="mb-8">
          <h2 className="text-2xl font-bold text-foreground mb-2">{v.title}</h2>
          {v.subtitle && <p className="text-sm text-text-muted">{v.subtitle}</p>}
        </div>
      )}

      <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-4">
        {v.features.map((f, i) => (
          <div
            key={i}
            className="group p-5 rounded-xl border border-border/50 bg-surface/50 hover:border-accent/30 transition-colors duration-200"
          >
            <div className="flex items-start gap-3">
              <FeatureIcon icon={f.icon} />
              <div className="min-w-0">
                <h3 className="text-sm font-semibold text-foreground mb-1">{f.title}</h3>
                <p className="text-xs text-text-muted leading-relaxed">{f.description}</p>
              </div>
            </div>
          </div>
        ))}
      </div>
    </div>
  );
}

